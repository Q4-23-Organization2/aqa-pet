package HomeWork_9;

public class Calculators {
    protected static double additionOperation(double firstOperand, double secondOperand) {
        return firstOperand + secondOperand;
    }
    protected static double subtractionOperation(double firstOperand, double secondOperand) {
        return firstOperand - secondOperand;
    }
    protected static double multiplicationOperation(double firstOperand, double secondOperand) {
        return firstOperand * secondOperand;

    }
    protected static double divisionOperation(double firstOperand, double secondOperand) {
        return firstOperand / secondOperand;
    }
}
