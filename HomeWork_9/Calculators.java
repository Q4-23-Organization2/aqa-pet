package HomeWork_9;

public class Calculators {
    public static double additionOperation(double firstOperand, double secondOperand) {
        return firstOperand + secondOperand;
    }
    public static double subtractionOperation(double firstOperand, double secondOperand) {
        return firstOperand - secondOperand;
    }
    public static double multiplicationOperation(double firstOperand, double secondOperand) {
        return firstOperand * secondOperand;

    }
    public static double divisionOperation(double firstOperand, double secondOperand) {
        return firstOperand / secondOperand;
    }
}
